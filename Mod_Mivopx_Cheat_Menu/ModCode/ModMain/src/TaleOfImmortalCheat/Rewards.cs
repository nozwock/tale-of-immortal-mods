	private static void CreateClothingRewards(string sexFilter, ref List<Item> targetList, List<Item> fallbackList, out int rewardId, string logName)
	{
		JsonReader.LoadClothJsonFile();
		List<ItemCloth> itemCloth = JsonReader.itemCloth;
		if (itemCloth != null && itemCloth.Count > 0)
		{
			targetList.Clear();
			foreach (ItemCloth item in JsonReader.itemCloth)
			{
				if (item.sex == sexFilter && int.TryParse(item.id, out var result))
				{
					targetList.Add(new Item
					{
						Id = result,
						Quantity = 1
					});
				}
				else if (item.sex == sexFilter)
				{
					ModMain.LogWarning("Skipping clothing item with invalid ID: " + item.id);
				}
			}
			ModMain.Log($"Added {targetList.Count} {logName} items from JSON.");
		}
		else
		{
			ModMain.LogWarning("itemCloth list is empty, using fallback manual list for " + logName + ".");
			targetList = new List<Item>(fallbackList);
			ModMain.Log($"Loaded {targetList.Count} {logName} items from manual fallback list.");
		}
		ModMain.Log("Creating " + logName + " rewards...");
		rewardId = Game.RewardFactory.CreateRewards(targetList);
	}
