	private static void CreateMountRewards()
	{
		try
		{
			JsonReader.LoadMountJsonFile();
			List<ItemHorse> itemHorses = JsonReader.itemHorses;
			if (itemHorses != null && itemHorses.Count > 0)
			{
				mountMaterialItems.Clear();
				foreach (ItemHorse itemHorse in JsonReader.itemHorses)
				{
					if (int.TryParse(itemHorse.id, out var result))
					{
						mountMaterialItems.Add(new Item
						{
							Id = result,
							Quantity = 1
						});
					}
					else
					{
						ModMain.LogWarning("Skipping mount item with invalid ID: " + itemHorse.id);
					}
				}
				ModMain.Log($"Loaded {mountMaterialItems.Count} mount items from JSON.");
			}
			else
			{
				ModMain.LogWarning("ItemHorses list is empty, using fallback manual list.");
				mountMaterialItems = new List<Item>(mountMaterialItemsOld);
				ModMain.Log($"Loaded {mountMaterialItems.Count} items from manual fallback list.");
